            int motorcycleCount = slotDao.countSlotsByType("Motorcycle");
            int truckCount = slotDao.countSlotsByType("Truck");
            int busCount = slotDao.countSlotsByType("Bus");

            totalCarSlotsLabel.setText(String.valueOf(carCount));
            totalMotorcycleSlotsLabel.setText(String.valueOf(motorcycleCount));
            totalTruckSlotsLabel.setText(String.valueOf(truckCount));
