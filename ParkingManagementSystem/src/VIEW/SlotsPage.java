    private void addNewSlotsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewSlotsBtnActionPerformed
        // TODO add your handling code here:
        try {
            int number = Integer.parseInt(slotNumberField.getText().trim());
            if (number <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Enter a positive number");
                return;
            }
            
            String lastSlot = "0";
            if (slotDao.countSlots() != 0){
                lastSlot = slotDao.getLastSlot().getSlotNumber();
            }

            for (int i = 1; i <= number; i++) {
                ParkingSlot slot = new ParkingSlot();

                int newNumber = Integer.parseInt(lastSlot) + i;
                slot.setSlotNumber(String.valueOf(newNumber));
                slot.setSlotType((String)slotTypeComboBox.getSelectedItem());
                slot.setIsOccupied(false);

                slotDao.createSlot(slot);
            }

            javax.swing.JOptionPane.showMessageDialog(this, number + " slots added successfully");

            updateSlotsLabel();
            slotNumberField.setText("");

        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid number entered");
        }
    }//GEN-LAST:event_addNewSlotsBtnActionPerformed

