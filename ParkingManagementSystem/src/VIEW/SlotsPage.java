    private void removeSlotsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeSlotsBtnActionPerformed
        // TODO add your handling code here:
        String slotType = (String) slotTypeComboBox.getSelectedItem();
        String numberText = slotNumberField.getText().trim();

        if (numberText.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter the number of slots to remove");
            return;
        }

        try {
            int numberToRemove = Integer.parseInt(numberText);

            if (numberToRemove <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Enter a positive number");
                return;
            }

            List<ParkingSlot> slotsToRemove = slotDao.findRemovableSlotsByType(slotType, numberToRemove);

            int successfullyRemoved = 0;

            if (slotsToRemove.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "No unoccupied " + slotType + " slots are available to remove");
                return;
            }

            for (ParkingSlot slot : slotsToRemove) {
                if (!slot.isIsOccupied()) {
                    slotDao.deleteSlot(slot.getSlotId());
                    successfullyRemoved++;
                }
            }

            if (successfullyRemoved > 0) {
                String message = successfullyRemoved + " unoccupied " + slotType + " slots removed successfully";

                if (successfullyRemoved < numberToRemove) {
                     message += "\n(Only " + successfullyRemoved + " slots were available for removal)";
                }
                javax.swing.JOptionPane.showMessageDialog(this, message);
            } else {
                 javax.swing.JOptionPane.showMessageDialog(this, "No slots were removed");
            }

            updateSlotsLabel();
            slotNumberField.setText("");

        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid number entered");
        } catch (Exception ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "An error occurred during slot removal", "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_removeSlotsBtnActionPerformed


